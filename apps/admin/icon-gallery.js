// Helper function to get icon with proper sizing and classes
export function getIcon(name, size = 24, className = '') {
  const icon = IconGallery[name];
  if (!icon) {
    console.warn(`Icon '${name}' not found in gallery`);
    return '';
  }

  return `<span class="icon ${className}" style="width: ${size}px; height: ${size}px; display: inline-flex; align-items: center; justify-content: center;">${icon}</span>`;
}

// Auto-replace emoji icons with professional SVG
export function replaceEmojiIcons() {
  const emojiMap = {
    '📊': 'dashboard',
    '🤝': 'network',
    '⚙️': 'settings',
    '👥': 'users',
    '👤': 'user',
    '🏢': 'building',
    '💎': 'star',
    '✉️': 'mail',
    '📅': 'calendar',
    '🔧': 'tool',
    '📝': 'fileText',
    '🚀': 'rocket',
    '⚡': 'zap',
    '☰': 'menu'
  };

  // Find elements with .icon-emoji class for replacement
  const emojiElements = document.querySelectorAll('.icon-emoji');
  emojiElements.forEach(element => {
    const emoji = element.textContent.trim();
    const iconName = emojiMap[emoji];
    if (iconName) {
      element.innerHTML = getIcon(iconName, 24, 'replaced-icon');
      element.classList.remove('icon-emoji');
      element.classList.add('icon-replacement');
    }
  });
}

// Initialize icons on data-icon elements
export function initializeIcons() {
  // Find all elements with data-icon attribute
  const iconElements = document.querySelectorAll('[data-icon]');

  iconElements.forEach(element => {
    const iconName = element.dataset.icon;
    const size = element.dataset.iconSize || 24;
    const className = element.dataset.iconClass || '';

    const iconSvg = getIcon(iconName, size, className);
    if (iconSvg) {
      element.innerHTML = iconSvg;
    }
  });

  // Also replace emoji icons
  replaceEmojiIcons();
}

// Make available globally
window.getIcon = getIcon;
window.initializeIcons = initializeIcons;
window.replaceEmojiIcons = replaceEmojiIcons;