/**
 * Helper function to get icon HTML with optional size and class
 * @param {string} name - Icon name from the gallery
 * @param {number} size - Icon size in pixels (default: 24)
 * @param {string} className - Additional CSS classes
 * @returns {string} HTML string of the icon
 */
export function getIcon(name, size = 24, className = '') {
  const icon = IconGallery[name];
  if (!icon) {
    console.warn(`Icon "${name}" not found in gallery`);
    return '';
  }
  
  const wrapper = document.createElement('div');
  wrapper.innerHTML = icon;
  const svg = wrapper.querySelector('svg');
  
  if (svg) {
    svg.setAttribute('width', size);
    svg.setAttribute('height', size);
    if (className) {
      svg.setAttribute('class', className);
    }
  }
  
  return wrapper.innerHTML;
}

/**
 * Icon component for React-like usage
 * @param {string} name - Icon name
 * @param {Object} props - Additional properties
 * @returns {HTMLElement} DOM element
 */
export function Icon(name, props = {}) {
  const { size = 24, className = '', color = 'currentColor', ...attrs } = props;
  
  const wrapper = document.createElement('span');
  wrapper.className = `icon-wrapper ${className}`.trim();
  wrapper.innerHTML = getIcon(name, size);
  
  const svg = wrapper.querySelector('svg');
  if (svg) {
    svg.style.color = color;
    Object.entries(attrs).forEach(([key, value]) => {
      svg.setAttribute(key, value);
    });
  }
  
  return wrapper;
}

